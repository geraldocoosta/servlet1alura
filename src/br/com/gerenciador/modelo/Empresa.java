package br.com.gerenciador.modelo;

public class Empresa {

	private String nome;
	private Integer id;

	public Integer getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

}
